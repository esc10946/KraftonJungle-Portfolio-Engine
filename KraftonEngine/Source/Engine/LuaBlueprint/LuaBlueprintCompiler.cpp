        void EmitForLoop(
            std::ostringstream&         Out,
            const FLuaBlueprintNode&    Node,
            int                         Indent,
            std::unordered_set<uint32>& ExecStack
        )
        {
            const FString First = GetInputExpression(Node, "First", "0");
            const FString Last  = GetInputExpression(Node, "Last", "0");
            const FString Guard = FString("__for_guard") + std::to_string(Node.NodeId);
            EmitIndent(Out, Indent);
            Out << "local " << Guard << " = 0\n";
            EmitIndent(Out, Indent);
            Out << "for __i = " << First << ", " << Last << " do\n";
            EmitIndent(Out, Indent + 1);
            Out << Guard << " = " << Guard << " + 1\n";
            EmitIndent(Out, Indent + 1);
            Out << "if " << Guard << " > __bp_loop_limit then error(\"LuaBlueprint for-loop iteration limit exceeded at node " << Node.NodeId << "\") end\n";
            LoopIndexNodeIds[Node.NodeId] = "__i";
            if (const FLuaBlueprintPin* Loop = FindPinByName(Node, "Loop", ELuaBlueprintPinKind::Output))
            {
                EmitNextExecFromPin(Out, Loop->PinId, Indent + 1, ExecStack);
            }
            LoopIndexNodeIds.erase(Node.NodeId);
            EmitIndent(Out, Indent);
            Out << "end\n";
            if (const FLuaBlueprintPin* Done = FindPinByName(Node, "Completed", ELuaBlueprintPinKind::Output))
            {
                EmitNextExecFromPin(Out, Done->PinId, Indent, ExecStack);
            }
        }

        void EmitWhileLoop(
            std::ostringstream&         Out,
            const FLuaBlueprintNode&    Node,
            int                         Indent,
            std::unordered_set<uint32>& ExecStack
        )
        {
            const FString Guard = FString("__guard") + std::to_string(Node.NodeId);
            EmitIndent(Out, Indent);
            Out << "local " << Guard << " = 0\n";
            EmitIndent(Out, Indent);
            Out << "while " << GetInputExpression(Node, "Condition", "false") << " do\n";
            EmitIndent(Out, Indent + 1);
            Out << Guard << " = " << Guard << " + 1\n";
            EmitIndent(Out, Indent + 1);
            Out << "if " << Guard <<
                    " > __bp_loop_limit then error(\"LuaBlueprint while-loop iteration limit exceeded at node " << Node.NodeId
                    << "\") end\n";
            if (const FLuaBlueprintPin* Loop = FindPinByName(Node, "Loop", ELuaBlueprintPinKind::Output))
            {
                EmitNextExecFromPin(Out, Loop->PinId, Indent + 1, ExecStack);
            }
            EmitIndent(Out, Indent);
            Out << "end\n";
            if (const FLuaBlueprintPin* Done = FindPinByName(Node, "Completed", ELuaBlueprintPinKind::Output))
            {
                EmitNextExecFromPin(Out, Done->PinId, Indent, ExecStack);
            }
        }

        // SpawnActor/SpawnPawn are data-output exec nodes.
        void EmitSpawnNode(
            std::ostringstream&         Out,
            const FLuaBlueprintNode&    Node,
            int                         Indent,
            std::unordered_set<uint32>& ExecStack
        )
        {
            const FString ClassName = LuaName(Node.NameValue);
            if (ClassName.empty())
            {
                AddDiagnostic(Result, ELuaBlueprintDiagnosticSeverity::Error, Node.NodeId, "Spawn node has no class name");
                return;
            }

            const FString Local    = FString("__n") + std::to_string(Node.NodeId);
            const FString Location = GetInputExpression(Node, "Location", "__bp_vec(0,0,0)");
            const FString Rotation = GetInputExpression(Node, "Rotation", "__bp_vec(0,0,0)");
            const FString Scale    = GetInputExpression(Node, "Scale", "__bp_vec(1,1,1)");
            const bool    bPawn    = Node.Type == ELuaBlueprintNodeType::SpawnPawn || Node.Type == ELuaBlueprintNodeType::SpawnPawnAndPossess;
            const bool    bPossess = Node.Type == ELuaBlueprintNodeType::SpawnPawnAndPossess;

            EmitIndent(Out, Indent);
            if (bPawn)
            {
                Out << "local " << Local << " = World.SpawnPawn(" << LuaQuoted(ClassName) << ", " << Location << ", " << Rotation << ", " << Scale << ", " << (bPossess ? "true" : "false") << ")\n";
            }
            else
            {
                Out << "local " << Local << " = World.SpawnActor(" << LuaQuoted(ClassName) << ", " << Location << ", " << Rotation << ", " << Scale << ")\n";
            }
            ExecLocals[Node.NodeId] = Local;
            EmitThen(Out, Node, Indent, ExecStack);
            ExecLocals.erase(Node.NodeId);
        }

        // {PinName} 형태의 placeholder 를 GetInputExpression 으로 치환해 한 줄짜리 statement 를 emit.
        // 단순 wrapper exec 노드 (DestroyActor, AddForce, SetActorLocation 등) 공통.
        void EmitSimpleCall(
            std::ostringstream&         Out,
            const FLuaBlueprintNode&    Node,
            int                         Indent,
            std::unordered_set<uint32>& ExecStack,
            const FString&              Template
        )
        {
            FString Line = Template;
            // 핀별로 {PinName} 치환. Exec/Object/Vector/Float/Bool 등 모두 대상.
            for (const FLuaBlueprintPin& Pin : Node.Pins)
            {
                if (Pin.Kind != ELuaBlueprintPinKind::Input) continue;
                if (Pin.Type == ELuaBlueprintPinType::Exec) continue;
                const FString PlaceholderToken = FString("{") + Pin.DisplayName.ToString() + "}";
                const FString Value            = GetInputExpression(Node, Pin.DisplayName.ToString().c_str(), "nil");
                // 단순 string replace — 같은 placeholder 가 여러 번 나올 수 있으므로 loop.
                size_t Pos = 0;
                while ((Pos = Line.find(PlaceholderToken, Pos)) != FString::npos)
                {
                    Line.replace(Pos, PlaceholderToken.size(), Value);
                    Pos += Value.size();
                }
            }
            EmitIndent(Out, Indent);
            Out << Line << "\n";
            EmitThen(Out, Node, Indent, ExecStack);
        }

        void ReplaceInputPlaceholders(FString& Line, const FLuaBlueprintNode& Node)
        {
            for (const FLuaBlueprintPin& Pin : Node.Pins)
            {
                if (Pin.Kind != ELuaBlueprintPinKind::Input || Pin.Type == ELuaBlueprintPinType::Exec)
                {
                    continue;
                }
                const FString PlaceholderToken = FString("{") + Pin.DisplayName.ToString() + "}";
                const FString Value = GetInputExpression(Node, Pin.DisplayName.ToString().c_str(), "nil");
                size_t Pos = 0;
                while ((Pos = Line.find(PlaceholderToken, Pos)) != FString::npos)
                {
                    Line.replace(Pos, PlaceholderToken.size(), Value);
                    Pos += Value.size();
                }
            }
        }

        void EmitExecResultCall(
            std::ostringstream&         Out,
            const FLuaBlueprintNode&    Node,
            int                         Indent,
            std::unordered_set<uint32>& ExecStack,
            const FString&              OutputName,
            const FString&              DefaultExpr,
            const FString&              CallTemplate,
            const FString&              GuardTemplate
        )
        {
            (void)OutputName;
            const FString Local = FString("__n") + std::to_string(Node.NodeId);
            EmitIndent(Out, Indent);
            Out << "local " << Local << " = " << DefaultExpr << "\n";

            FString Guard = GuardTemplate;
            FString Call = CallTemplate;
            ReplaceInputPlaceholders(Guard, Node);
            ReplaceInputPlaceholders(Call, Node);

            EmitIndent(Out, Indent);
            Out << "if " << Guard << " then " << Local << " = " << Call << " end\n";
            ExecLocals[Node.NodeId] = Local;
            EmitThen(Out, Node, Indent, ExecStack);
            ExecLocals.erase(Node.NodeId);
        }

